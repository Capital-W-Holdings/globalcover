export function getBenefitsByCategory(category: BenefitCategory): Benefit[] {
  return benefits.filter((b) => b.category === category);
}

export function getBenefitById(id: string): Benefit | undefined {
  return benefits.find((b) => b.id === id);
}

export function getBenefitCategoryBySlug(slug: BenefitCategory): Category | undefined {
  return benefitCategories.find((c) => c.slug === slug);
}

export function getExclusiveBenefits(): Benefit[] {
  return benefits.filter((b) => b.exclusive);
}

export function searchBenefits(query: string): Benefit[] {
  const lowerQuery = query.toLowerCase();
  return benefits.filter(
    (b) =>
      b.name.toLowerCase().includes(lowerQuery) ||
      b.provider.toLowerCase().includes(lowerQuery) ||
      b.description.toLowerCase().includes(lowerQuery)
  );
}
